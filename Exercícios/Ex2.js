numero_1 = Number(prompt("Digite o primeiro número:"))
numero_2 = Number(prompt("Digite o segundo número:"))

let soma = numero_1 + numero_2;
let subtracao = numero_1 - numero_2;
let multiplicacao = numero_1 * numero_2;
let divisao = numero_1 / numero_2;
let restoDiv = numero_1 % numero_2;

alert(`A soma dos dois números ${numero_1} e ${numero_2} é: ${soma} `)
alert(`A subtração dos dois números ${numero_1} e ${numero_2} é: ${subtracao} `)
alert(`A multiplicação dos dois números ${numero_1} e ${numero_2} é: ${multiplicacao} `)
alert(`A divisão dos dois números ${numero_1} e ${numero_2} é: ${divisao} `)
alert(`O resto da divisão dos dois números ${numero_1} e ${numero_2} é: ${restoDiv} `)